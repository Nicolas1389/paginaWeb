    let fechaKiss = document.getElementById('fechaKiss');
    let contenido2 = fechaKiss.textContent || fechaKiss.innerHTML;
    console.log(contenido2);

    ///// precio ////
    let precioKiss = document.getElementById('precioKiss');
    let contenido3 = precioKiss.textContent || precioKiss.innerHTML;
    console.log(contenido3);

    ///// tarjeta ////
